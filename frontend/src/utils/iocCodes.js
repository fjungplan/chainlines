export const getCountryName = (code) => {
    const found = IOC_CODES.find(c => c.code === code);
    return found ? found.name : code;
};

/**
 * Returns the Alpha-2 code for a given 3-letter IOC code.
 * Returns null if not found.
 */
export const getCountryFlag = (iocCode) => {
    if (!iocCode) return null;
    const found = IOC_CODES.find(c => c.code === iocCode.toUpperCase());
    return found ? found.iso2 : null;
};
